from django.db import models
from django.conf import settings
from django.conf import ValidationError
import uuid
from django.contrib.auth.models import User
from django.core.validators import MinValueValidator
from django.utils import timezone


class Listing(models.Model):
    """Represents a room or property available for booking."""
    owner = models.ForeignKey(User, on_delete=models.CASCADE, related_name='listings')
    title = models.CharField(max_length=255)
    description = models.TextField()
    address = models.CharField(max_length=255)
    price_per_night = models.DecimalField(max_digits=8, decimal_places=2, validators=[MinValueValidator(0)])
    is_available = models.BooleanField(default=True)
    created_at = models.DateTimeField(auto_now_add=True)

    def __str__(self):
        return self.title


class Booking(models.Model):
    """Represents a reservation made by a user for a listing."""
    user = models.ForeignKey(User, on_delete=models.CASCADE, related_name='bookings')
    listing = models.ForeignKey(Listing, on_delete=models.CASCADE, related_name='bookings')
    check_in = models.DateField()
    check_out = models.DateField()
    created_at = models.DateTimeField(auto_now_add=True)

    class Meta:
        constraints = [
            models.CheckConstraint(
                check=models.Q(check_out__gt=models.F('check_in')),
                name='check_out_after_check_in'
            )
        ]
        ordering = ['-created_at']

    def __str__(self):
        return f"Booking by {self.user.username} for {self.listing.title} ({self.check_in} to {self.check_out})"


class Review(models.Model):
    """Represents a review left by a user for a listing they booked."""
    user = models.ForeignKey(User, on_delete=models.CASCADE, related_name='reviews')
    listing = models.ForeignKey(Listing, on_delete=models.CASCADE, related_name='reviews')
    rating = models.PositiveSmallIntegerField(validators=[MinValueValidator(1)])
    comment = models.TextField(blank=True)
    created_at = models.DateTimeField(auto_now_add=True)

    class Meta:
        unique_together = ('user', 'listing')  # Prevent multiple reviews per user per listing
        ordering = ['-created_at']

    def __str__(self):
        return f"Review by {self.user.username} - {self.rating}/5"

